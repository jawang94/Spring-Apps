    @RequestMapping(value="/registration", method= RequestMethod.POST)
    public String registerUser(@Valid @ModelAttribute("user") User user, BindingResult result, HttpSession session) {
        userValidator.validate(user, result);
        if (result.hasErrors()) {
            return "/views/loginRegistration.jsp";
        } else {
            User u = userService.registerUser(user);
            session.setAttribute("userId", u.getId());
            return "redirect:/dashboard";
        }
    }

    @RequestMapping(value="/login", method=RequestMethod.POST)
    public String loginUser(@RequestParam("email") String email, @RequestParam("password") String password, RedirectAttributes attr, Model model, HttpSession session) {
        if (!userService.authenticateUser(email,password)) {
            String error = "Invalid Email or Password";
            attr.addFlashAttribute("error", error);
            return "redirect:/logreg";
        } else {
            User u = userService.findByEmail(email);
            Long id = u.getId();
            session.setAttribute("userId", id);
            return "redirect:/dashboard";
        }
    }


